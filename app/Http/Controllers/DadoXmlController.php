    {
        $codmodelo = $request->codmodelo;
        $xml = $request->strXml;
        $modelo = Modelo::findOrFail($codmodelo);
        $modelo->xml_modelo = $xml."\n";
        $result = $modelo->update();
        return \Response::json($result);
    }

